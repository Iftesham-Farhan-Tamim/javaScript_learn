// 'do-while' loops are known as 'exit control loops' the loop body is executed at least once before checking the condition.

let score = 11;

do {
    console.log(`Score is ${score}`);
    score++;
} while (score <= 10);

// Print:
// Score is 11

// Do while loop print the function first then check the condition







